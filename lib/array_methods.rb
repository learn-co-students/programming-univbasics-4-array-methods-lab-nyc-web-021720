def using_include(array, element)
 array.include?(element)
end
using_include(["tomato", "carrot", "lettuce"], "lettuce")

def using_sort(array)
 array.sort
end
using_sort(["shirts", "pants", "Hat", "shoes"])

def using_reverse(array)
  array.reverse
end
using_reverse(["Jpseph", "Mike", "Ryan", "Stephan"])

def using_first(array)
array.first
end
using_first(["dog", "cat", "bird", "rabbit"])

def using_last(array)
  
array.last
end
using_last(["battery", "skrew", "soap", "bulb"])

def using_size(array)
array.size
end
using_size(["Large", "Medium", "Small"])